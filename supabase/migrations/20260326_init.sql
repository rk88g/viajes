create table if not exists public.bookings (
  id uuid primary key default gen_random_uuid(),
  trip_id bigint not null references public.trips (id) on delete restrict,
  departure_id bigint not null references public.departures (id) on delete restrict,
  trip_title_snapshot text not null,
  departure_date_snapshot date not null,
  customer_name text not null,
  customer_email text not null,
  customer_phone text,
  seats_reserved integer not null check (seats_reserved > 0),
  total_amount numeric(10, 2) not null check (total_amount >= 0),
  status text not null default 'pending_payment' check (status in ('pending_payment', 'confirmed', 'cancelled', 'refunded')),
  payment_status text not null default 'unpaid' check (payment_status in ('unpaid', 'paid', 'refunded')),
  stripe_session_id text unique,
  stripe_payment_intent text,
  checkout_payload jsonb not null default '{}'::jsonb,
  paid_at timestamptz,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

